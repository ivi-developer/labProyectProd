public class VentanaInfo {

    private int index;
    private String rutaPJ;
    private String texto;
    private String rutaFondo;



    public VentanaInfo (String rutaPJ, String texto, String rutaFondo, int index)
    {
        this.index = index;
        this.rutaPJ = rutaPJ;
        this.texto = texto;
        this.rutaFondo = rutaFondo;
    }
}
